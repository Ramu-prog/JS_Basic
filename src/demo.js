// 1️⃣ Number
let num = 10;
console.log("Number:", num);

// 2️⃣ String
let str = "Hello";
console.log("String:", str);

// 3️⃣ Boolean
let isActive = true;
console.log("Boolean:", isActive);

// 4️⃣ Undefined
let x;
console.log("Undefined:", x);

// 5️⃣ Null
let emptyValue = null;
console.log("Null:", emptyValue);

// 6️⃣ BigInt
let bigNumber = 12345678901234567890n;
console.log("BigInt:", bigNumber);

// 7️⃣ Symbol
let uniqueId = Symbol("id");
console.log("Symbol:", uniqueId);

// 8️⃣ Object
let person = { name: "Ram", age: 25 };
console.log("Object:", person);
console.log(typeof (person))

// 9️⃣ Array (Special type of object)
let numbers = [1, 2, 3];
console.log("Array:", numbers);
console.log(typeof (numbers))

// 🔟 Function (Also object type)
function greet() {
    return "jhfi";
}

console.log("Function:", greet());





// Number → 10, 3.14
// String → "Hello"
// Boolean → true / false
//  Undefined → variable declared but no value use these
//  type assine the value and print it Null → intentional empty value BigInt → very large numbers Symbol → unique identifier